#include "minishell.h"

void	init_shell(t_shell *shell, char **envp)
{
	shell->input = NULL;
	shell->exit_status = 0;
	shell->commands = NULL;
	shell->envp = envp;
}

int	main(int argc, char **argv, char **envp)
{
	t_shell	shell;

	(void)argc;
	(void)argv;
	init_shell(&shell, envp);
	handle_input(&shell);
	return (0);
}
